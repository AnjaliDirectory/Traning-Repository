package Zensar.com.containment;

public class Refill {
	
	public String Inkcolor;
	public int lenght;
	public Nib nib;
	public String getInkcolor() {
		return Inkcolor;
	}
	public void setInkcolor(String inkcolor) {
		Inkcolor = inkcolor;
	}
	public int getLenght() {
		return lenght;
	}
	public void setLenght(int lenght) {
		this.lenght = lenght;
	}
	public Nib getNib() {
		return nib;
	}
	public void setNib(Nib nib) {
		this.nib = nib;
	}
	
	
	

}
